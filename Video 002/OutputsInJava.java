public class OutputsInJava {
    public static void main(String[] args) {
        System.out.print("Hello"); // cursor stays on the same line
        System.out.println("Hello"); // cursor goes onto the next line
        System.out.print("Hello\n");  // escape sequence cursor goes to new line

        System.out.println("1 2 3 4 5");
    }
}
